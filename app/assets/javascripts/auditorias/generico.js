convertir_fecha_str = function (fecha) {
  if (typeof fecha != 'undefined') {
    day = fecha.slice(0, 2)
    month = fecha.slice(3, 5)
    year = fecha.slice(6, 10)

    return month + "/" + day + "/" + year
  }
}

